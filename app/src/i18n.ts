const DICTS: Record<Locale, Dict> = { 'zh-CN': zhCN, 'zh-TW': zhTW, en, ja }

export function detectSystemLocale(): Locale {
  const langs = navigator.languages?.length ? navigator.languages : [navigator.language]
  for (const l of langs) {
    const low = (l || '').toLowerCase()
    if (low.startsWith('zh')) {
      if (low.includes('tw') || low.includes('hk') || low.includes('hant') || low.includes('mo')) return 'zh-TW'
      return 'zh-CN'
    }
    if (low.startsWith('ja')) return 'ja'
    if (low.startsWith('en')) return 'en'
  }
  return 'en'
}

/** current effective locale (set from settings by store) */
export const currentLocale = ref<Locale>(detectSystemLocale())

export function setLocale(l: Locale): void {
  currentLocale.value = l
  document.documentElement.lang = l
}

/** reactive translate; {var} placeholders via vars */
export function t(key: string, vars?: Record<string, string | number>): string {
  let s = DICTS[currentLocale.value][key] ?? DICTS['zh-CN'][key] ?? key
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v))
  return s
}

/** computed wrapper for templates that need reactivity via composition */
export const localeRef = computed(() => currentLocale.value)
